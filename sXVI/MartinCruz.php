												<h2>Martín de la Cruz </h2>
												<h3>1510 - 1555</h3>
											</header>
<p>Nací alrededor de 1510 en el barrio de Zacapan (lugar de zacate, en náhuatl), hoy Santa María Nativitas de Zacapan, Xochimilco. En esa época aun gobernaba el gran tlatoani mexica <a href="../sXV_anteriores/Moctezuma.php">Moctezuma Xocoyotzi </a>(1466-1520), poco antes de la caída de Tenochtitlán. Fui bautizado por los españoles como Martín de la Cruz. El conocimiento de la medicina tradicional era una costumbre muy arraigada en mi barrio. Vivíamos en las chinampas, rodeados de ahuejotes y ahí cultivábamos productos agrícolas y plantas curativas. Heredé gran parte de mis conocimientos de mis padres y abuelos y adquirí muchos otros por mi propia experiencia.</p>
    <p>Los primeros frailes franciscanos llegaron a Tenochtitlán en 1524. Quedamos admirados de su diferencia con los arrogantes conquistadores. Ellos andaban descalzos, con un viejo sayal. Vivían en casas pobres, durmiendo en un petate y comiendo tortillas de maíz con chile como nosotros.</p>
     <p>Con el apoyo del Virrey de Mendoza y de Fray Juan de Zumárraga, en 1536 los franciscanos fundaron el Real Colegio de Santa Cruz en Santiago Tlatelolco que tenía como objetivo la educación de muchachos indígenas. El Colegio se inició con alrededor de 100 niños, hijos de la nobleza indígena. Ahí se impartían clases de gramática, retórica, lógica, aritmética, geometría, astronomía y música. También se agregaron teología, religión, medicina y pintura. <a href="FBernardinoSahagun.php">Fray Bernardino de Sahagún</a> (1499-1590), fraile franciscano fue uno de los maestros del Colegio. Yo no estudié en el Colegio, pero trabajé como médico (<em>ticitl</em>) de los niños indios, junto con otro médico indígena Antón Hernández. Además también ejercí como examinador de otros indígenas practicantes del oficio. </p>
         <p>Desde 1520 se habían iniciado las epidemias virales de viruela y sarampión, enfermedades importadas del viejo continente. La devastadora epidemia de peste (<em>cocoliztli</em>) de 1545 acabo con la mayoría de los estudiantes del Colegio. Mis conocimientos y habilidades fueron muy reconocidos y recibí concesiones del primer virrey de la Nueva España Antonio de Mendoza y Pacheco (1490-1552). En los siguientes años, el <em>cocoliztli</em> sería responsable por la pérdida del 90% de la población indígena en México.</p>
       <p>Después de la partida del virrey Antonio de Mendoza con rumbo al Perú en 1550, recibí licencia del segundo virrey Luis de Velasco y Ruiz de Alarcón (1511-1564) para continuar con mis prácticas curativas. El Virrey también me concedió la autorización para montar en jaca (caballo) y llevar conmigo una ballesta en las salidas al campo. </p>
           <p>A los pocos años que murió el Conquistador Hernán Cortés (1485-1547), Francisco de Mendoza y Vargas (1523-1563), hijo del exVirrey Antonio de Mendoza, me encomendó una recopilación de las plantas medicinales para ser regalada al rey Carlos I de España (1500-1558). Su propósito era proponer la exportación de medicamentos desde la Nueva España y a su vez recibir apoyo para el Colegio de Santa Cruz. A su vez fray Jacobo de Grado, guardián y director del convento de Santiago Tlatelolco y presidente del Colegio de Santa Cruz, encomendó la traducción del texto en náhuatl al latín a mi paisano Juan Badiano, profesor del Colegio, también indígena originario del barrio de Chililico en Xochimilco. </p>
            <p>Tuve que trabajar muy rápidamente para terminar el manuscrito el 22 de julio de 1552 para que lo pudiera llevar el virrey a España entre agosto y septiembre de ese mismo año.  Yo titulé la obra en náhuatl <em>&quot;Amate-Cehuatl-Xihuitl-Pitli</em>&quot; y en latín quedó como el &ldquo;<a href="https://indd.adobe.com/view/ef9bfea9-94bc-4c06-8e54-b4c91c2f59cd"><em>Libellus de medicinalibus indorum herbis&rdquo;</em></a> o &ldquo;Librito de las hierbas medicinales de los indios&rdquo;, mejor conocido como Códice de la Cruz-Badiano. Esta recopilación se convirtió en uno de los principales documentos históricos sobre la medicina tradicional prehispánica. </p>
              <p>El documento constó de 63 hojas, divididas en trece capítulos, en las que además de los remedios, se incluyeron 184 ilustraciones a color de diferentes plantas. Las bellas ilustraciones a color fueron hechas por un grupo de <em>tlacuilos</em> (escribas) que representaron con precisión las plantas que incluí: sus raíces, flores, hojas e incluso el tipo de suelo en el que crecían. Las plantas son nombradas de acuerdo al sistema nativo de clasificación. Es un herbario medicinal que contiene remedios hechos a base de plantas, animales y minerales. </p>
                <p>Mi libro tiene cierta influencia europea tanto en su formato como en el contenido, sin embargo, recopilé importantes conocimientos indígenas sobre las prácticas curativas y terapéuticas. El manuscrito fue encuadernado en terciopelo rojo con filos en oro y fue entregado en 1553 al príncipe Felipe, en ausencia de su padre Carlos I. Después de la producción del &ldquo;librito&rdquo; continué mi trabajo como médico del Colegio de Tlatelolco. Se desconoce cuándo morí.</p>
                <p>Mi obra dio muchas vueltas. Francisco de Mendoza, hijo del Virrey de Mendoza lo envió a España a la biblioteca real en el monasterio de El Escorial. En el siglo XVII apareció en la biblioteca del farmacéutico de Felipe IV, Diego de Cortavila y Sanabria (1597-1657), quien seguramente lo tomó prestado sin registrarse. De ahí pasó a las manos del Cardenal Francesco Barberini (1597-1679), amigo del famoso astrónomo Galileo Galilei (1564-1642) y parte del tribunal que lo juzgó. La biblioteca del Cardenal pasó a formar parte de la Biblioteca Vaticana de Roma en 1902. Finalmente, en 1992, el Papa Juan Pablo II (1920-2005), en un acto sin precedentes, lo devolvió a México. Así que después de 439 años mi obra regresó al lugar de donde había partido. Actualmente se encuentra custodiada en la Biblioteca del Instituto Nacional de Antropología e Historia en la Ciudad de México.</p>
                   <p>Si te interesa puedes conocer más sobre la herbolaria, en la <a href="http://www.medicinatradicionalmexicana.unam.mx/" target="_blank">Biblioteca Digital de la Medicina Tradicional Mexicana de la UNAM </a></p>
