												<h2>Fray Bernardino de Sahagún</h2>
												<h3>1499 - 1590</h3>
											</header>
<p>Me llamo Bernardino de Ribeira. Nací en 1499 en la Villa de Sahagún, en el reino de Castilla al norte de España, lugar del cual tomé mi nombre. Estudié en la prestigiosa Universidad de Salamanca, la más antigua del mundo hispano. Posteriormente a los 18 años me uní a la orden de San Francisco y en 1524 me ordené de sacerdote.</p>
                                          <p>En 1529 nos embarcamos 20 religiosos bajo la dirección del franciscano Fray Antonio de Rodrigo, quien anteriormente ya había estado en el nuevo continente. Cinco años antes, habían llegado a Nueva España los primeros doce frailes franciscanos, entre ellos, Fray Toribio de Benavente, conocido como Motolinía. Después de un viaje de entre dos y cuatro meses desembarcamos en San Juan de Ulúa, Veracruz. Ahí pondría el pie sobre tierras de las que nunca volvería a salir. Camino hacia la gran Tenochtitlán y me encantó la lengua que hablaban los indígenas de Puebla, Tlalmanalco y Xochimilco, el náhuatl, el cual aprendí al poco tiempo. </p>
                                          <p>Al principio de mi llegada viví en Tlalmanalco (Hoy exConvento de San Luis Obispo), cerca de las faldas del Iztaccíhuatl y de ahí pase al convento de Xochimilco. Enseñé en el Colegio de la Santa Cruz de Santiago Tlatelolco fundado en 1536.  En esas épocas fue nombrado el primer Virrey de la Nueva España Antonio de Mendoza y Pacheco, representante de Carlos I de España.</p>
                                          <p>En el Colegio de la Santa Cruz de Tlatelolco también escribió el médico indígena <a href="MartinCruz.php">Martín de la Cruz</a> el libro&nbsp;&quot;<em>Libellus de medicinalibus indorum herbis</em>&quot;,&nbsp;sobre herbolaria, también conocido como Códice de la Cruz-Badiano. Su libro fue publicado en 1552 y contiene 185 ilustraciones de plantas medicinales. Además fue traducido al latín por el indígena xochimilca Juan Badiano. </p>
                                          <p>Trabajé en el valle de Puebla y la región de los volcanes y también en Tula y en Michoacán.  Después viví en el Convento de San Francisco de Tepeapulco,  cerca del cerro Xihuingo en donde hay un sitio arqueológico de la cultura Teotihuacana. Más tarde regresé a Tlatelolco (1571-1573) y al convento Grande de San Francisco en la Ciudad de México. Durante esta época llegó a Nueva España la expedición de <a href="FcoHToledo.php">Francisco Hernández de Toledo</a> que duraría hasta 1577. <br>
                                              <br>
                                            Trabajé como misionero, profesor y administrador. Pero gran parte de mi vida se la dedique a la escritura de mi obra titulada <strong>&ldquo;Historia general de las cosas de la Nueva España&rdquo;</strong>, basándome en información proveniente de fuentes directas, en testimonios de los sabios ancianos indígenas, así como en fuentes documentales, como los códices.</p>
                                          <p>Mi manuscrito se divide en doce libros que tratan sobre las formas de vida de los mexicas, así como de varios aspectos de su cultura. En particular, en el <strong><em>Libro XI</em></strong> recapitulo el conocimiento sobre zoología, botánica y mineralogía que tenían los nativos, describiendo en náhuatl sus características físicas, propiedades y el medio en el que vivían. Ahí incluyo 459 vocablos nahuas correspondientes especies animales; aproximadamente 200 hacen referencia a diferentes especies de aves, 50 aluden a una variedad de insectos, 40 a distintos tipos de mamíferos, 40 se utilizan para los reptiles y tortugas, mientras que el resto se emplea para crustáceos, moluscos y arácnidos.</p>
                                          <p>Mi obra la realice en etapas y le hice algunas revisiones que aparecieron  sucesivamente: los &ldquo;Primeros memoriales&rdquo;, el &ldquo;Códice Matritense&rdquo; y el &ldquo;Códice Florentino&rdquo;. Este último, considerado el manuscrito más completo, está escrito en náhuatl y en español e incluye más de 1850 ilustraciones. Fallecí a los 90 años en el convento Grande de San Francisco de México en 1950.</p>
