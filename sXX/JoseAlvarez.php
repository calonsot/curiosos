												<h2>José Álvarez del Villar</h2>
												<h3>1908-1989</h3>
											</header>
<p>José Álvarez fue un amante de la naturaleza, gusto que al principio encauzó dedicándose a la charrería y después al estudio de la ictiofauna dulceacuícola de México.</p>
                                          <p>Nació en Michoacán y ahí pasó su juventud. Tuvo una formación académica muy nutrida que lo llevó a mudarse varias veces. Primero viajó a la Universidad de California, Estados Unidos, donde se diplomó como técnico agrónomo. Más tarde se mudó a la ciudad de México  para cursar la carrera de químico-biólogo-parasitólogo en la Escuela Nacional de Ciencias Biológicas del IPN, y luego la carrera de biología en la misma institución (1938). </p>
                                          <p>A partir de entonces, le interesó estudiar la diversidad de peces y su cultivo para fines comerciales, así que regresó a Estados Unidos donde hizo una especialidad en piscicultura en el Instituto Politécnico de Alabama y  la maestría en ciencias en la Universidad de Míchigan. De regreso a tierras mexicanas, hizo el doctorado en ciencias de nuevo en el IPN.</p>
                                          <p>Sus investigaciones se enfocaron principalmente a estudiar la composición y la distribución de la fauna piscícola de diferentes cuerpos de agua dulce del país, en particular de su estado natal; incluso realizó estudios sobre peces fósiles. También le interesó hacer estudios sobre la piscicultura como fuente de alimentación rural.</p>
                                          <p>A partir de toda esta la información  recabada en sus trabajos, José escribió el &ldquo;Catálogo de los Peces de las Aguas continentales mexicanas&rdquo; y un par de manuales para la identificación y la clasificación de los peces.  </p>
                                          <p>Fue fundador y miembro de la Sociedad Mexicana de Hidrobiología. Tuvo varios cargos administrativos: fue consultor de la Secretaría de marina, asesor del Museo de Historia Natural de la Ciudad de México, o jefe de la Comisión para el Fomento de la Piscicultura rural. </p>
                                          <p>Gran parte de su vida también la dedicó a la docencia, incluso escribió dos libros de texto que por muchos años fueron ampliamente consultados.</p>
