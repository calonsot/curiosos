												<h2>Enrique Beltrán Castillo</h2>
												<h3>1903 - 1994</h3>
											</header>
<p>Nací en la Ciudad de México el 26 de abril de 1903. Mi padrino de bautizo fue el militar oaxaqueño Félix Díaz Prieto (1868-1945), sobrino del presidente Porfirio Díaz. A pesar de eso, yo fui simpatizante de la revolución. El interés por la biología me entró gracias a mi maestra de sexto de primaria, Matilde Guzmán de la escuela &quot;Pablo Moreno&quot; ubicada cerca del centro histórico del entonces Distrito Federal. Con ella aprendí a identificar animales usando el libro &quot;Animales mexicanos&quot; de Luis Murillo.</p>
<p>Estudié una carrera equivalente a lo que hoy es Biología en la Facultad Nacional de Altos Estudios de la Universidad Nacional (1920-1923), hoy Universidad Nacional Autónoma de México (UNAM) y me convertí en 1926 en el primer biólogo titulado de México. Mi maestro favorito fue <a href="../sXIX/AlfonsoLHerrera.php">Alfonso L. Herrera</a>. Realicé estudios de posgrado bajo la dirección del <a href="https://www.wikiwand.com/en/Gary_Nathan_Calkins">Dr. Gary N. Calkins</a> en la Universidad de Columbia, Estados Unidos. También hice prácticas en el <a href="https://www.wikiwand.com/en/Marine_Biological_Laboratory">Laboratorio de Biología Marina</a> en Woods Hole, Massachussets. </p>
<p>Me inicié estudiando la vida en pequeño: los protozoarios. Pero también me dediqué a la docencia, impartiendo cursos de zoología, biología general y protozoología en varios niveles y en varias instituciones: la <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sociedades/ENP.php">Escuela Nacional Preparatoria </a> y Facultad de Ciencias de la UNAM, la Escuela Nacional de Ciencias Biológicas del Instituto Politécnico Nacional, Escuela Nacional de Maestros y la Escuela Normal Superior de la Secretaría de Educación Pública.</p>
<p>En 1936 ayudé a reorganizar junto con Ángel Roldán, José Alcaraz y otros a la <strong><a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sociedades/SMHN.php">Sociedad Mexicana de Historia Natural</strong> </a>&quot; e iniciar su segunda época.</p>
<p>También me gustaba la investigación marina y así  proyecté la Estación de Biología Marina y el Museo Oceanográfico en Veracruz. En 1952 fundé y dirigí el Instituto Mexicano de Recursos Naturales Renovables, primera organización civil dedicada a la conservación en México.</p>
<p>Publiqué muchos artículos y varios libros sobre temas de protozoología, recursos naturales y conservación, historia de la ciencia, sociología y educación. Contribuí a construir los cimientos de la biología en México en el siglo XX, fui uno de los iniciadores del movimiento conservacionista y fundador de las primeras instituciones dedicadas a la investigación marina en el país. Fallecí el 23 de octubre de 1994 en la Ciudad de México.</p>
