												<h2>Enrique Beltrán Castillo</h2>
												<h3>1903 - 1994</h3>
											</header>
<p>Enrique Beltrán Castillo fue un biólogo que contribuyó a construir los cimientos de la biología en México en el siglo XX, iniciador del movimiento conservacionista y fundador de las primeras instituciones dedicadas a la investigación marina en el país. Nació en la Ciudad de México en 1903 y falleció en 1994.</p>
                                          <p>En la Escuela Nacional de Altos Estudios de la Universidad Nacional, hoy Universidad Nacional Autónoma de México (UNAM), estudió una carrera equivalente a la actual de Biología, convirtiéndose en 1926 en el primer biólogo titulado de México. Sus estudios de posgrado los realizó en la Universidad de Columbia, Estados Unidos.</p>
                                          <p>Su vida laboral comenzó estudiando los protozoarios, pero también se dedicó a la docencia, impartiendo cursos de zoología, biología general y protozoología en varios niveles y en varias instituciones: la Escuela Nacional Preparatoria y Facultad de Ciencias de la UNAM, la Escuela Nacional de Ciencias Biológicas del Instituto Politécnico Nacional, Escuela Nacional de Maestros y la Escuela Normal Superior de la Secretaría de Educación Pública. Se dedicó también a la investigación marina y así  proyectó la Estación de Biología Marina y el Museo Oceanográfico en Veracruz. En 1952 fundó y dirigió el Instituto Mexicano de Recursos Naturales Renovables, primera organización civil dedicada a la conservación en México.</p>
                                          <p>Publicó aproximadamente 500 artículos y 36 libros sobre temas de protozoología, recursos naturales y conservación, historia de la ciencia, sociología y educación.											</p>
