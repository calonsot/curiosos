												<h2>Efraim Hernández Xolocotzi</h2>
												<h3>1913 - 1991</h3>
											</header>
<p>Nací el 23 de enero de 1913 en San Bernabé Amaxac de Guerrero, muy cerca de la ciudad de Tlaxcala. Mis padres fueron Luis Hernández Xolocotzi y Bibiana Guzmán. Mi papá era campesino y mi mamá era maestra de primaria. Yo fui el menor de tres hermanos. Apenas tenía dos años cuando nos fuimos a la Ciudad de México, donde estudié en la "Escuela Protectora del Niño". A los 10 años, me mudé con mi mamá a Nuevo Orleans en Estados Unidos, donde mis dos hermanos mayores ya eran residentes. Yo cursé los estudios de educación básica en la primaria y secundaria N. 35 y la preparatoria en la Escuela Stuyvesant High de Nueva York.</p>
<p>Mi formación profesional la realicé en el Colegio Estatal de Agricultura N.Y., posteriormente en la Escuela de Agricultura de la Universidad de Cornell N.Y. Pero fue un viaje a México cuando tenía 20 años lo que definió mi vocación como agrónomo.</p>
<p>A mi regreso a México trabajé en el Banco Nacional de Crédito Ejidal en el estado de Tabasco. A partir de ese momento me interesé por los problemas que había en el agro mexicano, a través de mis recorridos a distintos lugares del país y el trato con los campesinos. Decidí tomar el apellido paterno de mi abuela Micaela por honor al campesinado y en lugar de Guzmán tomé Xolocotzi. En 1941 conocí a Lily Santamaría Ramírez con quien tuvimos a tres hermosas hijas: Ruth, Diana y Silvia.</p>
<p>A principios de la década de los cuarenta, fui Asesor Técnico para la Administración Económica Extranjera de la Embajada Americana, donde trabajé para aumentar la producción de semillas oleaginosas de México. Por estos años me dediqué además a recolectar las razas de maíz existentes en México. Gran parte de este material fue destinado a la fundación Rockefeller, que fue la principal responsable de formar las colecciones del Centro Internacional de Mejoramiento de Maíz y Trigo (CIMMYT).</p>
<p>Entre 1947 y 1949 estudié la maestría en Artes con especilidad  en Biología en la Universidad de Harvard, en Cambridge Massachissets. Inicié el doctorado posteriormente pero lo abandoné cuando mi esposa falleció en 1953.</p>
<p>Durante más de cuarenta años dediqué mi vida a la enseñanza agrícola y etnobotánica, así como a las investigaciones sobre plantas útiles de América Latina y México. Mis alumnos y esclavos me llamaban &quot;Maestro Xolo&quot;. Entre los más destacados estuvieron <a href="MiguelAMartinez.php">Miguel Ángel Martinez Alfaro</a>, Samuel Levy Tacher y <a href="JoseSarukhan.php">José Sarukhán Kermez</a>. Trabajé en la Escuela de Agricultura del Tecnológico de Monterrey, el Centro de Botánica del Colegio de Posgraduados, la Escuela Nacional de Agricultura (ENA) en Chapingo que después se convirtión en la Universidad Autónoma de Chapingo (UACH).</p>
<p>Escribí alrededor de 200 artículos científicos y seis libros. Colaboré con Enrique Beltrán, fundador del Instituto Mexicano de Recursos Naturales Renovables (IMERNAR). Junto con mis grandes amigos Faustino Miranda y su esposa Enriqueta García escribimos en 1963 &quot;Los tipos de vegetación de México y su clasificación&quot;. Para promover el estudio de las dioscoreas, plantas precursoras de las píldoras anticonceptivas, se creó La Comisión para el Estudio de las Dioscóreas (1959-1970). En esta comisión dirigida por Enrique Beltrán con <a href="FaustinoMiranda.php">Faustino Miranda</a> y yo como asesores, se realizaron las primeras investigaciones sobre ecología vegetal. Entre los alumnos que participaron en este gran proyecto estuvieron <a href="JoseSarukhan.php">José Sarukhán Kermez</a>, <a href="ArturoGomezP.php">Arturo Gómez Pompa</a> y Javier Chavelas Polito.</p>
<p>En 1976 inicié el programa Tecnología Agrícola Tradicional y empecé a enseñar etnobotánica. Ahi enfatizaba que las comunidades indígenas espacios de creación y conservación del germoplasma. Sus prácticas garantizan la conservación de la biodiversidad y de la agrobiodiversidad.<br>
  Fallecí el 21 de febrero de 1991 en mi casa por complicaciones de la diabetes a los 78 años. Mis cenizas fueron esparcidas en el Campus Universitario de Chapingo y mi obra escrita más relevante ha sido compilada en la colección Xolocotzia editada por la Universidad Autónoma de Chapingo. Algunas de las fotografías que tome en el campo se pueden encontrar en el <a href="http://bdi.conabio.gob.mx/fotoweb/Grid.fwx?archiveId=5064&columns=4&rows=8&search=Efrain%20Hernández%20Xolocotzi" target="_blank">Banco de Imagenes de CONABIO</a>.</p>
