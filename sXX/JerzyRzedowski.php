												<h2>Jerzy Rzedowski Rotter </h2>
												<h3>1926</h3>
											</header>
                <p>Nací el 27 de diciembre de 1926 en la ciudad de Leópolis (Lwów), Polonia, hoy Ucrania. Fui hijo único de Arnold Rzedowski y Ernestina Rotter. En Silesia, Polonia comencé mi formación académica. Desde entonces me atraía la naturaleza y a menudo salía con mi papá a hacer excursiones al campo. Silesia estaba cerca al río Odra y a las montañas de los Sudetes, cerca de Checoslovaquia, hoy república Checa. </p>
                <p>Sin embargo, durante la Segunda Guerra Mundial, debido a nuestra ascendencia judía fuimos puestos por los nazis por 20 meses en varios campos de concentración, entre ellos el de Auschwitz. En 1946 al finalizar la guerra nos liberaron y me mudé junto con mi padre a México, donde vivía una tía. Mi padre, se quiso ir a Israel, pero yo decidí quedarme en México.</p>
                <p>Mientras aprendía español trabajé como traductor en la Embajada de Polonia, ya que conocía el polaco, ruso, alemán e inglés. Estudié la carrera de Biología en la <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sociedades/ENCB.php"> Escuela Nacional de Ciencias Biológicas </a>del Instituto Politécnico Nacional realizando mi tesis sobre la vegetación del Pedregal de San Ángel bajo la dirección del <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXX/FedericoBonet.php">Dr. Federico Bonet. </a>Durante estos años conocí a la bióloga Graciela Calderón Diaz Barriga con la me casaría en 1954. Graciela nació en Salvatierra, Guanajuato y nos encontramos en la Escuela Nacional de Ciencias Biológicas. Desde entonces hemos trabajado juntos en el conocimiento de la flora de México.</p>
				<p>El Pedregal de San Ángel me interesó ya que es la vegetación crece en una corriente de lava creada por la erupción del volcán Xitle hace 1,600 años. Era una gran oportunidad de conocer como se establecen las comunidades vegetales y poco a poco se van haciendo más complejas. Cuando lo visité por primera vez estaba muy bien conservado, solamente una parte estaba fragmentado por el ferrocarril que iba a Cuernavaca y por la prolongación de la avenida Insurgentes, pero se iniciaba la construcción del fraccionamiento del Pedregal y de Ciudad Universitaria. En mi tesis clasifiqué <a href="http://www.repsa.unam.mx/documentos/Rzedowski_1994_Vegetacion_del_Pedregal.pdf">la vegetación </a>siguiendo el famoso sistema de la escuela de fitosociología del botánico suizo Josias Braun-Blanquet (1884-1980).</p>
                <p>Al terminar la licenciatura ingresé a laborar como investigador en la Universidad de San Luis Potosí. En esos años colaboré con la fundación del Instituto de Investigación de Zonas Desérticas y promoví la creación del primer herbario estatal de San Luis.</p>
                <p>En 1958 realicé una estancia en Francia donde hice estudios de ecología vegetal y aprendí los métodos europeos de investigación en este campo. En 1959 comencé a laborar en el Colegio de Posgraduados de Chapingo, además de obtener el Doctorado en Ciencias por la Facultad de Ciencias de la Universidad Nacional Autónoma de México, bajo la dirección del <a href="FaustinoMiranda.php">Dr. Faustino Miranda</a>.</p>
                <p>Por más de 20 años fui profesor e investigador de la Escuela Nacional de Ciencias Biológicas del Instituto Politécnico Nacional invitado por el Dr. Juan Manuel Gutiérrez Vásquez. En mi larga vida de enseñanza he tenido mucho estudiantes que se han interesado en la flora de México como Luz María Arreguín, Rafael Fernández Nava, Raquel Galván, Sergio Zamudio, Socorro González Elizondo (la presidente de mi club de fans), Yolanda Herrera Arrieta, Irene García, Gerardo García Regalado, Miguel Medina Cota, Miguel Angel Barrios,  Luz del Socorro Rodríguez Jiménez, José Magaña, Xavier Madrigal Sánchez y Lydia Guiridi Gómez, y el <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXX/GastonGuzman.php">Dr. Gastón Guzmán </a>de quien, a pesar de entusiasmarse por los hongos, dirigí su tesis doctoral.  
                <p>Fuera de la academia trabajé con la <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXX/LuzMaVillareal.php">Maestra Luz María Villarreal de Puga, </a>quien fundó el Instituto de Botánica en la Universidad de Guadalajara y con el <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXX/FranciscoTakaki.php">Biol. Francisco Takaki </a>del Instituto Nacional de Geografía y Estadística (INEGI). El Biol. Takaki, junto con el Ing. Ramón Cardoza, impulsaron la creación de la Dirección General de Uso de Suelo y Vegetación y lograron que se colectaran e identificaran las especies representativas de la vegetación de México. </p> 
                <p>Soy cofundador, y actual editor, de la revista <em>Acta Botánica Mexicana </em>, una de las publicaciones más importante en este campo a nivel nacional. Asimismo, desarrollé el Centro Regional del Bajío del Instituto de Ecología A.C., en Pátzcuaro Michoacán y soy coeditor de la serie <em>Flora del Bajío y de Regiones Adyacentes</em>. He publicado alrededor de 120 artículos de investigación, nueve libros y varias decenas de capítulos de libros. Una de mis obras más conocidas <em>La Vegetación de México </em>(1978), es una referencia clásica en la literatura botánica. En el XVI Congreso Internacional de Botánica fui nombrado "Botánico del Milenio". Puedes conocer muchos de los lugares por donde anduve y las plantas que fotografié en el <a href="http://bdi.conabio.gob.mx/fotoweb/archives/5063-Jerzy%20Rzedowski/">Banco de Imágenes de CONABIO</a></p>
