												<h2>Laura Huerta Múzquiz</h2>
												<h3>1913 - 2000</h3>
											</header>
<p>Laura Huerta Múzquiz fue una destacada química, bacterióloga parasitóloga, pionera en los estudios de las algas en México. Con ella comenzó una nueva época de la ficología nacional, intensificándose la recolección de algas por colectores mexicanos y creándose importantes herbarios en el país. Nació en 1913 en Coahuila y murió en el año 2000.</p>
                                          <p>En 1934 se mudó a la Ciudad de México y ahí se incorporó a la recién creada Escuela de Bacteriología, Parasitología y Fermentaciones de la Universidad Gabino Barreda, posteriormente convertida en la Escuela Nacional de Ciencias Biológicas del Instituto Politécnico Nacional (ENCB).</p>
                                          <p>Sus primeros estudios los realizó sobre hormonas y enzimas de plantas y animales en el Laboratorio de Fisiología General Vegetal de dicho instituto, al que se integró en 1938. Posteriormente realizó una estancia en el Instituto para la Investigación de Hormonas de Plantas en Estados Unidos. Sin embargo, fue a raíz de una expedición oceanográfica organizada en 1955 por la ENCB, que comenzó su interés por el estudio de las algas marinas. A partir de este momento se dedicó a trabajar sobre la florística y ecología de estos seres, incluyendo investigaciones sobre el fitoplancton y algunos aspectos sobre su aprovechamiento.</p>
                                          <p>Publicó más de 20 artículos científicos sobre las algas del Golfo de México, la Península de Yucatán, las costas de Oaxaca, Colima, Sinaloa y Baja California. También se dedicó a la docencia, impartió clases de citología vegetal, botánica criptogámica y ecología de algas. </p>
