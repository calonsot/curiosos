												<h2>Miguel Ángel de Quevedo</h2>
												<h3>1862 - 1946</h3>
											</header>
<p>¡Hola! Me llamo Miguel Ángel José Ignacio de Quevedo Zubieta y nací en la hermosa ciudad de Guadalajara, Jalisco en 1862. En ese entonces el presidente de México, era el oaxaqueño Benito Juárez. Mi papá se llamaba José Valente García de Quevedo Portillo y mi mamá María de los Ángeles Zubieta y Maldonado. Desafortunadamente, mis padres murieron cuando yo era pequeño y junto con mis hermanos viaje a Francia, en donde el tío Bernabé Quevedo, canónigo de la Catedral de Bayona, se haría cargo de nosotros. La ciudad de Bayona se encuentra en el suroeste de Francia muy cerca del mar Cantábrico y de los Pirineos. Ahí, en Francia, estudié en la Universidad de Burdeos (1883) y en la Escuela Politécnica (1887) cuando se inició la construcción de la famosa Torre Eiffel. A los 26 años (1888) regresé a México para ejercer mi profesión. El presidente en ese entonces era el general Porfirio Díaz. Cuando cumplí los 30 (1892) me casé con la veracruzana María Adolfina de la Luz Carrara y Cevallos con quien tuvimos dos preciosas hijas, Ángela y Dolores.</p>
                                          <p>Trabajé en temas muy diversos. Fui supervisor de las obras de drenaje en el Valle de México, construidas para evitar las inundaciones en la ciudad. Impulsé la creación de reservas y parques nacionales a lo largo del país, con el propósito de asegurar el buen funcionamiento de los bosques y proteger la vida silvestre. Promoví la creación de viveros para la reforestación de la ciudad y sus alrededores, inclusive doné parte de mi rancho &ldquo;Panzacola&rdquo; y es posible que lo hayas visitado. Fue declarado Parque Nacional por el Presidente Lázaro Cárdenas en 1938. Hoy en día se conoce como los &ldquo;Viveros de Coyoacán&rdquo;. Ahí está el &ldquo;<em>arboretum</em>&rdquo; o colección de mis árboles favoritos: liquidámbar, fresno, ahuehuete, cedro blanco, olmo chino, almez, varias especies de pinos y algunos eucaliptos.</p>
                                          <p>Fundé la Escuela Nacional Forestal (1921), pionera en la enseñanza de este campo en México, así como la Sociedad Forestal Mexicana (1922) y su publicación &ldquo;México Forestal&rdquo;. La revista fue muy importante en la difusión del manejo forestal o silvicultura y la formulación de políticas mexicanas de aquellos años. Fundé la celebración al Día del Árbol, que celebrábamos el segundo jueves del mes de julio, apoyado por el oaxaqueño José Vasconcelos, creador de la Secretaría de Educación Pública y  la escritora chilena Gabriela Mistral. Para conmemorar ese día reforestabamos gran cantidad de áreas. Fui asesor en la creación de la ley Forestal en 1926 y fungí como Jefe del Departamento Forestal de la Secretaría de Agricultura y Recursos Hidráulicos. </p>
                                          <p>Morí a los 73 años satisfecho por las numerosas acciones que emprendí para evitar la deforestación de los bosques mexicanos a través de su conservación y reforestación. Por lo anterior me dieron el título de &ldquo;Apóstol del árbol&rdquo;.</p>
