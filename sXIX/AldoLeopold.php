												<h2>Aldo Leopold</h2>
												<h3>1887 - 1948</h3>
											</header>
											<p>Nací el 11 de enero de 1887  en Burlington Iowa. Mi padre Carl Leopold era un empresario que se dedicó a la manufactura de escritorios de nogal. Mi madre Clara Starker, era prima de mi papá. En esas épocas se acostumbraban los matrimonios entre primos. Mi abuelo materno fue un imigrante alemán que se dedicó a la ingeniería y a la arquitectura. Mi lengua materna fue el alemán, pero aprendí muy pequeño el inglés. Fui el primero de cuatro hermanos: Mary Luize, Carl Starker, Frederic y yo. </p>
											<p>A los 17 años ingresé en la Escuela Laurenceville, en Nueva Jersey, para preparme a entrar a la Escuela Forestal de Yale. Después de un año me aceptaron en Yale. En 1909 tuve mi primer trabajo en Arizona y Nuevo México con el Servicio Forestal. Durante mi estancia en Nuevo México en 1912 me casé con Estella Bergere, una maestra que conocí en Santa Fé . Su familia de origen español, vivía en esta región desde el siglo XVII. Tuvimos cinco hijos: <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXX/StarkerLeopold.php">Starker,</a> Luna, Nina y Carl y Estella.  </p>  
											<p>En 1924 me llevé a la familia a Madison, Wisconsin en donde trabajé en el Laboratorio de Productos Forestales. Años más tarde entré al Departamento de Economía Agrícola en la Universidad de Wisconsin como profesor de Manejo de Vida Silvestre.  </p>
											<p>A principios del siglo XX, las políticas de "conservación" en Estados Unidos incluían la remoción de depredadores como lobos, pumas, coyotes y línces de las áreas protegidas. La idea era promover más venados para la cacería recreativa. Esta política trajo muchas consecuencias negativas. Por eso me gustaba visitar México, en particular la cuenca del Río Gavilán en la Sierra Madre Occidental. Ahi podía estudiar los ecosistemas con buena salud. En septiembre de 1936 hice un gran viaje al Río Gavilán, con mi amigo Ray Roark donde pudimos cazar y pescar por un par de semanas. Para cazar utilizábamos arco y flecha. Había que ser deportista y no tomar ventaja de venados y guajolotes. Además podíamos ver águilas, trogones y pericos y por la noche escuchar los aullidos del lobo mexicano. Era un paraíso. </p>
											<p>En 1937 regresé a la misma región acompañado de mi hermano Carl y de mi hijo <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXX/StarkerLeopold.php">Starker</a>. Ahí me inspiré para escribir el ensayo que llamé "Canción del Río Gavilán" - <br><br>“… En una noche tranquila, cuando la fogata está baja y las Pleyades se han trepado por arriba de las rocas, siéntate quieto y escucha el aullido de un lobo, y piensa profundamente en todo lo que has visto y tratado de entender. Entonces, quizá la escuches —una gran armonía que late —su puntuación incluida en las miles de colinas, sus notas son las vidas y muertes de plantas y animales, sus ritmos cubren segundos y siglos". </p>
											<p>En 1937 el Presidente Lázaro Cárdenas apoyado por el Jefe del Departamento Forestal y de la Caza y Pesca, <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXIX/MiguelAQuevedo.php">Arq. Miguel Ángel de Quevedo</a>, decretó como Reserva Forestal Nacional y Zona de Refugio de la Fauna Silvestre "Campo Verde" a una zona de 1,080.67 km2 los municipios de Casas Grandes y Madera en el occidente de Chihuahua en el límite con Sonora. Dentro de esta zona quedó el inolvidable Río Gavilán.</p>
											<p>En los años 30 compré poco más de 30 hectáreas en el condado Sand cerca del río Wisconsin. Era un granja abandonada con un gallinero. Ahi me llevaba a la familia a pasar los fines de semana. Convertimos el gallinero en una cabaña, hicimos una hortaliza, y sembramos alrededor de 40 mil árboles. Increiblemente, a pesar de las condiciones extremas del clima, el terreno se convirtió en un maravilloso bosque lleno de vida. Actualmente se considera un Sitio Histórico. Ahi escribí el libro por el que me conoce mucha gente: El Almanaque del Condado Sand que se publicó en 1949.  Fallecí de un ataque cardíaco mientras intentaba apagar un incendio en el terreno de un vecino a nuestra cabaña a los 61 años el 21 de abril de 1948.</p> 
