											<!-- InstanceBeginEditable name="Biografía" -->
											<p>Nací en el hermoso poblado de Temascalcingo ("el pequeño temazcal"), Estado de México el 6 de julio de 1840. Mi padre fue Felipe Velasco y mi madre María Antonia Gómez-Obregón. Mis padres me dieron el interminable nombre de José María Tranquilino Francisco de Jesús Velasco Gómez-Obregón de Velasco. Mis dos hermanos Ildefonso y Antonio fueron reconocidos médicos.</p>
                                            <p>Realicé mis estudios primarios en la ciudad de México, donde llegué a los 9 años. Mi padre murió de una epidemia de cólera a los pocos meses de nuestra mudanza, así que empecé a trabajar desde muy jóven.</p> 
                                            <p> A los 15 años en 1855 ingresé a la Academia de San Carlos. Ahí fui alumno del pintor italiano Eugenio Landesio (1810-1879) recién llegado a México y quien me inició en la pintura de paisajes. Durante las clases me di cuenta de la necesidad de conocer profundamente aquello que dibujaba y pintaba así que para complementar mis obra artística ingresé a la Academia de Medicina donde cursé botánica, zoología, anatomía, arquitectura, geografía, física y matemáticas. Recibí el nombramiento de "maestro de Perspectiva" de manos del presidente Benito Juárez. Comencé a dar clases de pintura a los 18 años y en 1860 gané un concurso de pintura y con el premio pude aliviar mis problemas económicos. Entre mis estudiantes destacadados estuvieron Diego Rivera, David Alfaro Siqueiros y José Clemente Orozco, reconocidos muralistas.</p>
                                            <p>En 1868 comencé a colaborar en &ldquo;La Flora del Valle de México<em>&rdquo;</em>, una obra de botánica por entregas, en la cual hacía dibujos para ilustrar los fascículos. Ese mismo año me casé con Luz Sánchez Armas Galindo y tuvimos una batallón de 13 hijos de los cuales ocho alcanzaron la mayoría de edad. En 1889 representé al Gobierno de México en la Exposición Universal de París para la cual se había construido la maravillosa Torre Eiffel. Ahi recibí la ccondecoración de Caballero de la Legión de Honor. Al igual que mi hermano Ildefonso, fui miembro de la <a href="https://www.biodiversidad.gob.mx/biodiversidad/curiosos/sociedades/SMHN.php">Sociedad Mexicana de Historia Natural (SMHN) </a>donde participaban los hombres de ciencia más reconocidos del país. Ahi realicé muchas láminas que servían de apoyo gráfico para los artículos de otros autores que trataban temas de botánica, zoología, geología y paleontología. Por ejemplo, ilustré los artículos sobre colibríes de <a href="https://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXIX/ManuelMVillada.php">Manuel M. Villada </a>y de Rafael Montes de Oca. En esta sociedad, además de ilustrador científico, fui secretario, vicepresidente y presidente interino. </p>
                                            <p>La publicación de la Sociedad se llamaba <em>La Naturaleza</em>, y en ella publiqué mis investigaciones. Mi primer aporte como hombre de ciencia fue un estudio botánico sobre las cactáceas de México. Mi obra biológica más importante fue &ldquo;Descripción, metamorfosis y costumbres de una especie nueva del género <em>Siredon</em>&rdquo;, con ésta di a conocer datos precisos y hasta entonces desconocidos de la metamorfosis del famoso ajolote mexicano.</p>
                                            <p>Por más de 35 años, trabajé en el <a href="https://www.biodiversidad.gob.mx/biodiversidad/curiosos/sociedades/MNHN.php">Museo Nacional </a>como investigador. Uno de los productos de mi estancia en el museo fue la realización de varios lienzos en los que representé la evolución de la vida y las eras geológicas. En 1905 después de un ataque al corazón me retiré a pintar a Villa de Guadalupe Hidalgo, cerca de la Basílica de Guadalupe, donde fallecí el 26 de agosto de 1912. Mi querido pueblo actualmente se llama "Temascalcingo de José María Velasco".</p>
