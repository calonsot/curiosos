												<h2>Alfonso Herrera Fernández</h2>
												<h3>1838 – 1901</h3>
											</header>
<p>Alfonso Herrera Fernández fue un destacado naturalista que vivió el tránsito hacia la constitución de la biología como disciplina científica. Su padre fue Francisco Herrera Tovar y Guzmán y su Madre Rosario Fernández San Salvador Tamayo. Nació en la ciudad de México en 1838 y falleció en Morelos en 1901. Se graduó como farmacéutico en la Escuela Nacional de Medicina en 1858.   </p>
                                          <p>Como asiduo promotor y difusor de las ciencias naturales participó en la comisión encargada de estructurar un sistema educativo nacional que se concretó en el nacimiento de la Escuela Nacional Preparatoria en 1867. Dentro de esta escuela puso particular énfasis en el estudio de la historia natural y promovió la creación de la Sociedad Científica &ldquo;Antonio Alzate&rdquo;, de la que además fue presidente. Se casó con Adela López Hernández ese mismo año, con la que tuvo tres hijos, Román, Alfonso y María Adelaida Braulia Ángela. <strong>Alfonso L. Herrera</strong> siguió los pasos de su padre y se convirtió en un ilustre científico.</p>
                                          <p>Fue jefe de la Comisión Científica Mexicana (1883) que daría origen al Instituto Médico Nacional. Escribió el libro &ldquo;Nueva Farmacopea Mexicana<em>&rdquo;</em> (1884) de la Sociedad Farmacéutica de México, donde incluye la descripción de muchas sustancias medicinales así como los usos dados y el conocimiento de la herbolaria indígena tradicional. Publicó más de un centenar de artículos tanto de divulgación como investigación sobre temas químicos, farmacológicos, botánicos y zoológicos. Sus artículos se publicaron en <em>La Naturaleza </em>(revista de la Sociedad Mexicana de Historia Natural) y <em>La Gaceta Médica</em> (de la Academia Mexicana de Medicina). Ejemplos de éstos son: &ldquo;Observaciones sobre los hongos comestibles&rdquo; y &ldquo;Apuntes para la helmintología mexicana&rdquo;.</p>
                                          <p>Impartió clases de historia natural, botánica y zoología en la Escuela Nacional de Agricultura, la Escuela Nacional de Medicina, la Escuela Nacional Preparatoria y Escuela Normal de Profesores. Perteneció a numerosas sociedades científicas como: la Academia Médica de México; la Sociedad Mexicana de Historia Natural (de la que fue cofundador); Sociedad Farmacéutica; la Academia Mexicana de Ciencias exactas, físicas y naturales; entre muchas otras.</p>
