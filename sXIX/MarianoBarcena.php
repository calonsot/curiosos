												<h3>1842 - 1899</h3>
											</header>
<p>Mariano Santiago de Jesús de la Bárcena Ramos fue ingeniero, político y un hombre de ciencia muy prolífico. Nació en Jalisco en 1842 y murió en ese mismo estado en 1899.</p>
                                          <p>En 1864 inició sus estudios en la Real Academia de San Carlos y más tarde continuó en la Escuela Nacional Preparatoria. Posteriormente pasó a ser estudiante de Ingeniería en la Escuela de Minas. Fue miembro de la Sociedad Filomatemática Mexicana, de la Sociedad Mexicana de Historia Natural y  socio de la Sociedad Mexicana de Geografía y Estadística.</p>
                                          <p>En el campo de las ciencias naturales describió y clasificó especies vegetales y dio nombre a dos especies fósiles. Además, por su trabajo en la Secretaría de Fomento, participó en diferentes comisiones de exploración a lo largo del territorio mexicano. De la información recabada derivaron sus tratados de geología, paleontología y litología: <em>Noticia geológica del estado de Aguascalientes</em>, <em>Las obsidianas de México</em>, <em>Datos para el estudio de las rocas mesozoicas de México y sus fósiles</em> y <em>Noticia acerca del hallazgo de restos humanos prehistóricos en el valle de México</em>.</p>
                                          <p>En 1877 se creó el Observatorio Meteorológico Central, precursor del Servicio Meteorológico Nacional, del cual fue su director hasta su muerte. Ahí coordinó las investigaciones sobre el clima y el tiempo; creía que la observación minuciosa de éstos durante años, le daría elementos para establecer reglas sobre los fenómenos periódicos de la vegetación y se podrían establecer &ldquo;calendarios botánicos&rdquo;.</p>
                                          <p>También desempeñó cargos en la política, fue nombrado secretario de gobierno de Jalisco así como gobernador de ese estado por un breve periodo.</p>
