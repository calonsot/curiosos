												<h2>Isaac Ochoterena Mendieta</h2>
												<h3>1885 - 1950</h3>
											</header>
<p>Nací en 1885 en Atlixco, Puebla. Mi vida escolar comenzó en Puebla, pero años más tarde me mudé junto con la familia a la Ciudad de México. Ahí estudié el bachillerato en la Escuela Nacional Preparatoria. Tras la muerte de mi padre regresó a Puebla para comenzar mi vida laboral como profesor, trabajo que continué años después en los estados de Durango y San Luis Potosí. También desempeñé otros cargos como el de Inspector de Educación Pública y Director General de Educación. Paralelamente, realicé exploraciones a varias regiones de México como a la Sierra Madre Occidental y Oriental, de las que derivaron mis primeras publicaciones sobre la vegetación y el relieve de México, comenzando así mi gusto por la botánica y en especial por las increibles cactos.</p>
                                          <p>Posteriormente regresé a la Ciudad de México, donde en 1916 ingresé a la Dirección de Estudios Biológicos, y a partir de 1921 me desempeñé como maestro universitario en distintas dependencias de la Universidad Nacional Autónoma de México. En 1943 ingrese como miembro del Colegio Nacional.</p>
                                          <p>Publiqué más de 200 escritos en varias áreas de la biología además de la botánica, como la reproducción celular, cancerología, histología comparada, genética y embriología. Se interesó grandemente en la evolución biológica, particularmente en las ideas del naturalista inglés Carlos Darwin, y en el origen de la vida, temas que introduje en mi libro <em>Lecciones de Biología</em> de 1922 y  <em>Tratado Elemental de Biología</em>. Creo que contribuyí significativamente a la construcción e institucionalización de la biología moderna en México. En 1950 fallecí en la Ciudad de México.</p>
