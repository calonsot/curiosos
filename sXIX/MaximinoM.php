												<h2>Maximino Martínez</h2>
												<h3>1888 - 1964</h3>
											</header>
<p>Maximino Martínez fue un destacado botánico que nació en 1888 en San Miguel Regla, estado de Hidalgo, y murió en la Ciudad de México en 1964.</p>
                                          <p>Cursó sus estudios primarios en su tierra natal, donde más tarde obtuvo el título de Profesor de Instrucción Primaria. En 1908 se mudó a la capital del país para estudiar en la Escuela Normal donde se recibió y fue profesor. De 1914 a 1929 fungió como Encargado de la Sección de Botánica del Museo Nacional de Historia Natural.</p>
                                          <p>Dentro de la botánica, su principal interés fueron los grupos forestales de los pinos, piceas, romerillos y encinos, de los que describió  varias especies nuevas e hizo estudios sobre su distribución geográfica.</p>
                                          <p>En 1941 junto con un grupo de investigadores fundó la Sociedad Botánica de México,  siendo director por varios años y editor del boletín de la sociedad durante catorce. También fue investigador al Instituto de Biología de la Universidad Nacional Autónoma de México e impartió clases en la Escuela Nacional Forestal y en la Escuela Nacional de Ciencias Biológicas del Politécnico Nacional, donde fundó un Herbario. </p>
                                          <p>Entre sus trabajos se encuentra el libro <em>Catálogo de nombres vulgares y científicos de plantas mexicanas</em> (1923), magna obra en la que incluyó los nombres de las plantas en náhuatl y que además dio pie para la publicación de posteriores ediciones aumentadas: <em>Las plantas más útiles que existen en la República Mexicana</em> (1928) y <em>Las plantas medicinales de México</em> (1934), obra considerada de gran importancia por la vasta información que contiene sobre la herbolaria mexicana.</p>
