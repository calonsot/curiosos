												<h2>Antonio del Castillo Patiño</h2>
												<h3>1820 - 1895</h3>
											</header>
<p>Nací en Pungarabato (hoy Ciudad Altamirano), en el límite entre Guerrero y Michoacán el 17 de junio de 1820, fui el cuarto hijo del Coronel José Antonio del Castillo y María Marcelina Patiño. Mi padre más tarde se convirtió en gobernador de San Luis Potosí. Mi madre fue la que me hizo agarrarle interés a las actividades científicas. A los 12 años me mudé a la ciudad de México a estudiar en una de las mejores escuelas que pertenecía al caballero francés Matyen de Fossey. Después ingresé al Colegio de Minería y me gradué en 1845. Mas tarde fui director y profesor por muchos años de mineralogía, geología y paleontología en la misma institución. Ahí tuve la oportunidad de presentar las nuevas teorías científicas del geólogo inglés Charles Lyell (1797-1875), plasmadas en su libro de "Principios de Geología", el cual tuvo gran influencia sobre el naturalista inglés<a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXIX/DarwinWallace.php">Charles R. Darwin.</a></p>
<p>Consideraba que la enseñanza de estas disciplinas debía sustentarse en una práctica sistemática de exploración, recopilación de muestras, análisis de laboratorio y formación de colecciones; así que acostumbraba llevar frecuentemente a mis alumnos en excursiones geológicas a diferentes partes de la República para hacer colectas y registros.</p>
<p>Establecí relaciones con los políticos de ese momento así como geólogos de otros países, para impulsar la investigación geológica. De esta manera participé en la fundación de la Comisión Geológica de México, destinada a elaborar cartas geológico-mineras, un catálogo descriptivo de especies minerales de México y otro de fósiles.</p>
<p>También contribuí a la creación de la Escuela Práctica de Minas de Pachuca y del Instituto de Geología Nacional (hoy Instituto de Geología de la UNAM) y a la fundación de la Sociedad de Mineros de la República (1883). Fuí el primer presidente y socio fundador de la <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sociedades/SMHN.php">Sociedad Mexicana de Historia Natural</a> junto con José Joaquín Arriaga, Francisco Cordera y Hoyos, Gumesindo Mendoza, <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXIX/AlfonsoHerrera.php">Alfonso Herrera (padre),</a> Antonio Peñafiel, Manuel Río de la Loza, Manuel Urbina y Altamirano, y <a href="http://www.biodiversidad.gob.mx/pais/curiosos/sXIX/ManuelMVillada.php">Manuel M. Villada.</a></p>
<p>A pesar de ser un Ingeniero en Minas, (nadie es perfecto), fui considerado el primer paleontólogo mexicano pues recolecté y describí fósiles de mamíferos del Valle de México, con los que hice el primer catálogo oficial de estos vertebrados en el país. Fallecí el 27 de octubre de 1895, a la edad de 75 años.</p>
