												<h2>Enrique Rioja Lo-Bianco</h2>
												<h3>1895 - 1963</h3>
											</header>
<p>Enrique Rioja Lo-Bianco fue un biólogo pionero en el estudio de los gusanos marinos (poliquetos) en España y en México. Nació en España en 1895 y falleció en la ciudad de México en 1963. Estudió en la Universidad de Madrid la licenciatura de Ciencias Naturales y obtuvo el doctorado por su investigación sobre fauna anélida. En su país se convirtió en especialista en zoología de invertebrados marinos e hidrobiología. </p>
                                          <p>Debido a la guerra civil española llegó como exiliado a México en 1939. Fue invitado a integrarse como investigador al Instituto de Biología de la UNAM. Ahí ocupó la jefatura del Departamento de Hidrobiología donde creó el Laboratorio de Hidrobiología, al que consolidó como centro de investigación.</p>
                                          <p>Participó en la estructuración de los planes de estudio de la Facultad de Ciencias de la UNAM, en la preparación de los Anales del Instituto de Biología (publicación de dicho instituto) y en los planes para la creación del Museo de Historia Natural. </p>
                                          <p>Realizó varias exploraciones a lo largo de la costa del Pacífico mexicano, producto de las cuales registró más de 300 especies de poliquetos (algunas de ellas no conocidas anteriormente) de las cuales más de 43 son válidas todavía en la actualidad.</p>
                                          <p>Realizó más de 200 trabajos de investigación sobre poliquetos, crustáceos, esponjas de agua dulce y temas variados de hidrobiología. Varios de estos trabajos contienen ilustraciones realizadas por él mismo. Además de dedicarse a la docencia tanto en la UNAM como en el Instituto Politécnico Nacional, escribió libros de texto y de divulgación como son <em>Tratado elemental de zoología</em>, <em>La vida en el mar</em>, <em>Zoología hispanoamericana</em> (invertebrados) y <em>El mar, acuario del mundo</em>, entre otros.</p>
