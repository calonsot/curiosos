											<header>
												<h2>Carl Lumholtz </h2>
												<h3>1851-1922</h3>
											</header>
<p>Nací en Fåberg, Noruega el 23 de abril de 1851. El pueblo era pequeño pero vivíamos a la orilla de un hermoso río rodeado de bosques. Mi padre Paul Nicolai Lumholtz era oficial de la armada noruega. Mi madre Inger Elise Lumholtz. Fui el mayor de siete hermanos. Estudié teología, por decisión de mi padre, en la Universidad de Christiania en Oslo (1876) cuando se conocía como la Real Universidad Frederick. Sin embargo, me interesaba más salir a explorar y colectar animales. Afortunadamente mi profesor Robert Collet, quien más tarde fue director del Museo de Zoología de la Universidad de Oslo, me consiguió un lugar en una expedición a Australia como colector de plantas y animales para el Museo de Historia Natural de la Universidad.</p>

<p>“Que desgracia sería morir sin ver la Tierra completa”<p>

<p>La convivencia con los pueblos originarios de ese país y el registro de sus formas de vida durante cuatro años de exploración (1880-1884) dio origen a mis primeros trabajos etnográficos. En Australia aprendí que la mejor manera de conocer la historia natural de plantas y animales era vivir con los aborígenes. Por 14 meses viví y viajé con una tribu de aborígenes australianos. Describí al simpático canguro arbóricola (<i>Dendrolagus lumholtzi</i>) de las selvas húmedas del noreste de Australia que hoy lleva mi apellido. </p>
<p>Más tarde conseguí el apoyo del Museo Americano de Historia Natural, la Sociedad Geográfica Americana (ambos de Estados Unidos) y del entonces presidente de México, Profirio Díaz para comenzar mis exploraciones por el territorio nacional.</p>
<p>En total emprendí seis viajes de investigación entre 1890 y 1910, en los que recorrí principalmente amplias zonas del norte y occidente de México. Así pude convivir y fotografiar a los raramuri, tepehuanos, coras, huicholes, purépechas, nahuas, pápagos y tarascos. También hice registros arqueológicos, del paisaje, la flora y la fauna de varios de los estados que visité.</p>
<p>El resultado de mis estudios y esfuerzos culminaron con la publicación del voluminoso libro <a href="http://www.gutenberg.org/files/16426/16426-h/16426-h.htm">“El México Desconocido” </a>(1902), originalmente escrito en inglés (1902) y después traducido al español (1904). Esta obra es a la fecha un referente para los estudios sociales de la época del Porfiriato así como etnográficos por sus numerosos datos, descripciones, anécdotas y material fotográfico.</p>
<p>Viajé también a otros lugares del mundo como la India y Borneo. Publiqué mi autobiografía en un libro titulado "Mi vida de exploración" en 1921 y al prepararme para un nuevo viaje a Nueva Guinea, fallecí de tuberculosis en el lago Saranac en las montañas de Adirondack en Nueva York, el 5 de mayo de 1922 a los 71 años. He sido considerado uno de los pioneros de la antropología mexicana debido a mi amplio trabajo en campo sobre diferentes pueblos indígenas del país.</p>

<p>“Nos enseñan a ver a la gente primitiva como sinónimo de crudo, malo y vicioso. Nada podría ser más erróneo….  

La mente indígena está mejor desarrollada y sus sentidos están mejor entrenados que los del hombre blanco; su intelecto y claridad de pensamiento es más alto que el promedio de la gente común en Europa y América. … 
Aunque se viste en harapos, nace siendo un caballero.”</p>

<p>“Es antinatural carecer de amor especial por el país donde nacimos, así como el hombre tiene más afecto a su familia que por otras familias. 

Pero dejemos que nuestra lealtad se extienda a todo el globo en el que viajamos por el universo, y esforcémonos en servir a la humanidad, más que a nuestro propio país.”</p>

														<p>Carl S. Lumholtz</p>


