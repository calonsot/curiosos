<p>George Boole Hinton fue un ingeniero minero y botánico inglés y uno de los colectores de plantas en México más reconocidos durante el siglo XX. Nació en Londres, hijo de Charles Howard Hinton y Mary Everest Boole, ambos matemáticos y escritores. A la edad de siete años se mudó con su familia a Japón y después a Estados Unidos, donde estudió minería en varias universidades. En ese país trabajaba como ensayador y visitaba minas en México durante el verano. El país le gustó tanto que decidió convertirlo en su hogar junto con su esposa Emily Percival Wattley en 1911. </p>
                                          <p>Ya en territorio nacional, se desempeñó en diversas profesiones: ingeniero civil, ensayista, ingeniero metalúrgico, arquitecto, industrial y finalmente colector botánico. El director del Real Jardín Botánico de Kew, el botánico Sir Arthur Hill (1875-1941), le propuso recolectar especímenes de plantas durante su trabajo en la mina El Rincón en Temascaltepec, en el estado de México.</p>
                                          <p>A sus cincuenta años, Hinton se dedicó de tiempo completo a la botánica. Hizo numerosas expediciones por el territorio nacional acompañado de James Hinton (1915-2006), el más pequeño de sus tres hijos, quién también se convirtió en prominente botánico y escritor. Padre e hijo, de excepcional condición física, recorrieron las selvas secas de las sierras de Guerrero y Michoacán viajando en mula, atravesando zonas que eran prácticamente desconocidas para la botánica debido a lo accidentado del terreno y a la inseguridad de los caminos. También recorrieron Coahuila, Nuevo León, Tamaulipas, el Estado de México y Oaxaca. George Hinton descubrió seis géneros y 360 especies de plantas nuevas para la ciencia.</p>
                                          <p>Del total de cada colecta, una parte la preparaba para venderla a herbarios, jardines botánicos e instituciones de diferentes países, como Inglaterra, Suiza y Estados Unidos.  Algunas de sus colectas se encuentran en el Herbario Nacional del Instituto de Biología de la Universidad Nacional Autónoma de México y en el Herbario de la Escuela Nacional de Ciencias Biológicas del Instituto Politécnico Nacional. Otra parte de la colecta la guardaba para su propio herbario &ldquo;G.B. Hinton&rdquo;, en Nuevo Léon, que contiene más de 20 mil ejemplares de plantas, ahora al cuidado de su nieto George Sebastián Hinton (1949-), la tercera generación de colectores botánicos.</p>
