												<h2>Pablo de la Llave y Fernández de Ávila</h2>
												<h3>1773 – 1833</h3>
											</header>
<p>Soy jarocho de nacimiento, nací en la ciudad de Córdoba, Veracruz el 11 de febrero de 1773. Mis padres fueron Francisco Antonio de la Llave y María Gertudis Fernández de Ávila y Solís. Fuimos ocho hermanos y cuatro hermanastros. Cuando tenía 12 años perdí a mi papá quien era capitán en México al servicio del rey español. Manuel, mi hermano mayor siempre me apoyó en mis estudios y me envió a estudiar a la ciudad de México, llamada en aquellos tiempos la &ldquo;Ciudad de los Palacios&rdquo;, por sus enormes y elegantes edificios. Estudié en el Colegio de San Juan de Letrán, creado desde 1548, que estaba en las calles que ahora se llaman Venustiano Carranza y Madero del Centro Histórico de la Ciudad de México. </p>
    <p>A la edad de 28 años siendo sacerdote y habiendo impartido clases de filosofía tomé un barco rumbo a España con la finalidad de seguir estudiando. Estando allá, sentí curiosidad por los distintos conocimientos escritos en otros idiomas así que estudié francés, luego hebreo y después algunos idiomas llamados &ldquo;lenguas muertas&rdquo;. Pero sin duda lo que más me fascinó fue el estudio de las plantas, al cual dediqué gran parte de mi tiempo. </p>
    <p>Entre los años de 1811 y 1812 fui alumno y amigo del renombrado naturalista novohispano <a href="JoseMMucino.php">José Mariano Mociño</a>, quien residía en España desde 1803. Pasábamos los días en Madrid clasificando y nombrando científicamente a las plantas procedentes de la Real Expedición Botánica de la Nueva España (México) que se realizó de 1787 a 1803. Las plantas se rescataron cuando el imperio francés invadió España. También ordenamos las aves del Real Gabinete de Historia Natural de Madrid. ¡Era sorprendente ver aves tan coloridas originarias de África, centro y sur América! Fui nombrado director del Real Jardín Botánico de Madrid e incluso di algunas clases de botánica. </p>
        <p>Años más tarde en España fui electo diputado representando a mi país de origen en la Corte Real, pero cuando los españoles se enteraron que apoyaba las ideas de independencia de México, me persiguieron hasta hacerme prisionero. Mientras tanto en México la mitad de mi fortuna se utilizó para financiar la independencia de México llevada a cabo por Don José María Morelos y Pavón.</p>
         <p>Finalmente regresé a México en el año de 1823, a través de Francia, ya que no había relaciones entre España y México. Aquí dividí mi vida entre cargos políticos (Ministro de Justicia y de Hacienda), eclesiásticos y el estudio por las plantas que llamaba mucho mi atención. Me parecía interesantísimo darle nombre a la flora, así que realicé un estudio de las orquídeas de Michoacán y una descripción de líquenes. También investigué a las hormigas de la miel y algunas aves. En todas ellas descubrí nuevos géneros y especies.</p>

         <p>Las orquídeas se me hacían en particular interesantes, ya que tienen varias características únicas. Es una de las familias de plantas más diversas del mundo. La mayoría son epífitas, esto quiere decir que viven trepadas en los árboles, pero sin hacerles daño. Así que tomas sus nutrientes del agua de lluvia y de la humedad ambiental. Sus flores son muy hermosas y especializadas, uno de sus pétalos tiene forma, tamaño y color distinto a los demás. Ahí aterrizan los polinizadores, como abejas, abejorros y mariposas.</p>   

        <p>Fui director del <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sociedades/MNHN.php">Museo Nacional de Historia Natural </a>de México en 1831 y del <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sociedades/RJBPVNE.php">Jardín Botánico del Palacio Nacional. </a>Dediqué el nombre científico del quetzal (<em>Pharomachrus moccino</em>) a mi gran amigo y maestro José Mariano Moccino. Además dediqué junto con <a href="JoseMartinez.php">Juan Martínez de Lejarza</a> algunos géneros de plantas a la memoria de nuestros caudillos de la independencia como: <em>Hidalgoa</em>, <em>Allendea</em>, <em>Abasoloa</em>, <em>Casimiroa </em>y <em>Morelosia</em>.</p>
            <p>El género Gomezia lo dediqué a Juan Antonio Gómez de Guevara originario de Cantabria España quien introdujo el cultivo de los mangos y el café en Orizaba, Veracruz.<br>
            Fui miembro de varias sociedades científicas y literarias de Europa.  <br>
            A principios del año de 1833 fui llevado enfermo a la Hacienda del Corral en el cantón de Córdoba, con la finalidad de encontrar el alivio a una extraña enfermedad en el estómago, pero todo fue inútil. Dejé mi país para siempre en junio de ese año a la edad de 60 años.</p>
