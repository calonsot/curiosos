												<h3>1737 - 1799</h3>
											</header>
<p>José Antonio Alzate y Ramírez nació en 1737 en Ozumba, estado de México, y murió en 1799. Fue un sabio sacerdote que se interesó por el estudio de las matemáticas, física, astronomía y las ciencias naturales. Su vida religiosa inició en el Colegio de San Ildefonso bajo la orden de los jesuitas, y más adelante, ingresó a la Real y Pontificia Universidad de México donde obtuvo el bachillerato en artes seguido por el bachillerato en teología.</p>
                                          <p>Alzate es reconocido porque además de interesarse por la generación de nuevos conocimientos a través de la observación y la experimentación, se preocupó seriamente porque éstos resultaran de alguna utilidad para los habitantes. Como parte de esta preocupación por vincular la ciencia con la sociedad, se convirtió en el iniciador de la divulgación científica en México y fue uno de los grandes periodistas del siglo XVIII del país. Entre sus publicaciones científicas más importantes se encuentran: <em>Asuntos varios sobre ciencias y artes, Observaciones sobre la física, historia natural y artes útiles </em>y<em> Gaceta de México</em>. En ellas incluía información sobre novedades científicas y literarias de su época, así como variados aspectos científicos, muchos de ellos dedicados a la medicina, tema sobre el cual Alzate escribió más de 50 artículos.</p>
                                          <p>En su honor se formó en 1884 la Sociedad Científica Antonio Alzate que en 1935 se convirtió en la Academia Nacional de Ciencias de México.</p>
