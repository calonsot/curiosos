												<h2>Francisco Xavier Clavijero Echegaray</h2>
												<h3>1731 - 1787</h3>
											</header>
<p>Nací en el Puerto de Veracruz el 9 de septiembre de 1731. Mi padre fue Blas Clavigero, nacido cerca de Valladolid, España y mi madre María Isabel Echegaray, criolla, de ascendencia vasca. Fuimos 11 hermanos.  Desde pequeño tuve la oportunidad de viajar con mi padre y toda la familia por muchas regiones de la Nueva España, conociendo su impresionante naturaleza. De Veracruz nos mudamos a la Sierra de Puebla en Teziutlán y después a Jamiltepec, en la mixteca baja de Oaxaca. Compartí mis aventuras con los indígenas de México y además aprendí náhuatl desde muy temprano.</p>

<p>Estudié en Puebla en el Colegio de San Jerónimo, perteneciente a los jesuitas y más tarde (1716-1788) ingresé a la orden de los Jesuitas en el Convento de Tepotzotlán (hoy Estado de México), donde aprendí varios idiomas. También me interesé por los escritos de René Descartes, Isaac Newton, Gottfried Leibniz, entre otros. En 1754 fui ordenado sacerdote. Pasé tiempo en la Ciudad de México, en Morelia y en Guadalajara, pero el 27 de febrero de 1767, el rey Carlos III de España (1716-1788) expulsó a los jesuitas de España y de sus colonias. En esas fechas el virrey en Nueva España era Carlos Francisco de Croix (1703-1778). Después de un viaje complicado terminé en Bolonia, en el norte de Italia, junto con muchos otros compañeros jesuitas. </p>

<p>Fui religioso, historiador, docente y naturalista. En Europa me di cuenta de la ignorancia que había sobre el continente americano y sus habitantes. Ahí se creía que los indígenas americanos eran inferiores. En 1780 publiqué en italiano la "Historia Antigua de México", escrita en el exilio en Bolonia, Italia. Fueron diez libros que incluyeron la historia natural, el pasado prehispánico, los mexicanos, y la conquista. En estos libros combatí la visión de los europeos sobre la población y la naturaleza de América. por ejemplo, escribí “Animales del reino de México” refutando al naturalista francés Georges Louis Leclerc, conde de Buffon (1707-1788) sobre la inferioridad de los animales americanos. Para esto, analicé cuidadosamente la obra de <a href="https://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXVI/FcoHToledo.php">Francisco Hernández de Toledo.</a> Durante el siglo XVIII mis libros se publicaron en inglés y en alemán.

<p>Además escribí nueve Disertaciones, en donde defendí a los indígenas de la Nueva España, en contra de las calumnias de los europeos como el filósofo holandés Cornelius De Paw, el historiador escocés William Robertson y escritores y naturalistas franceses Guillaume-Thomas Raynal, y el conde de Buffon. Algunos contemporáneos con los que simpatizaba en los temas que escribí fueron el antropólogo novohispano Antonio de León y Gama, el sacerdote naturalista <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXVIII/JoseAlzate.php">José Antonio Alzate, </a>Lorenzo Boturini y el jesuita Pedro José Márquez, entre otros.</p> 

<p>Mis principales publicaciones fueron: </p>
<li>1780 <i>Storia Antica de Messico </i>(en italiano) (4 volúmenes), que se publicó en alemán (1789-1790) y en español (1826).
<br>En el apéndice de Historia de México incluí nueve disertaciones. 
<li>Primera: Población de América y particularemente de México
	<li>Segunda: Principales épocas de la historia del reino de México
	<li>Tercera: Tierra del reino de México
	<li>Cuarta: Animales del reino de México
	<li>Quinta: Constitución física y moral de los mexicanos
	<li>Sexta: La cultura de los mexicanos
	<li>Séptima: Confines y población de los reinos de Anahuac
	<li>Octava: Religión de los mexicanos
	<li>Novena: Origen del mal francés</li><br>

<li>1789 Storia Antica ó Baja California (en italiano), en español (1852).
<p>
<p>Mi libro <i>Storia Antica de Messico, </i> fue utilizado por el naturalista <a href="https://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXVIII/AlexanderHumboldt.php">Alexander von Humboldt </a>cuando el describió a la nueva España. Fallecí en Bolonia el 2 de abril de 1787 a los 55 años en los brazos de mi hermano Ignacio Clavigero, también jesuita. El 5 de agosto de 1970 mis restos fueron trasladados de Bolonia a la Ciudad de México, y actualmente descanso en la "Rotonda de las Personas Ilustres" en el Panteón Civil de Dolores, en la tercera sección de Chapultepec.
