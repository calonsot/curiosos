												<h2>José Mariano Mociño</h2>
												<h3>1757 - 1820</h3>
											</header>
<p>Nací en Real y Minas de Temascaltepec, hoy estado de México en 1757. Mis padres se llamaron Juan Antonio Mosiño y Manuela Losada. Realice estudios de teología en el Real y Pontificio Seminario Tridentino de México. Me casé a los 21 años (1778) con María Rita Rivera y Melo Montaño y nos mudamos a vivir a la ciudad de Oaxaca. Más tarde regresé solo a la capital a estudiar medicina en la Real y Pontificia Universidad de México. Ahí conocí al sacerdote naturalista <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXVIII/JoseAlzate.php">José Antonio Alzate y Ramírez </a>(1737-1799) quien me contagió su interés por la botánica.</p>
<p>Apenas inaugurado el Real Jardín Botánico de la Nueva España en 1788, tomé un curso de botánica con el médico y botánico español Vicente Cervantes Mendo. En 1785 se descubrió el manuscrito de la primera expedición a Nueva España del médico y naturalista español <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXVI/FcoHToledo.php">Francisco Hernández de Toledo </a>(1514-1517), protomédico de Felipe II (1527-1598). El manuscrito original se pensaba perdido debido al incendio de la biblioteca de El Escorial en 1671 donde se encontraba resguardado. Así que después de 200 años, se inició la organización de la segunda expedición: la Real Expedición Botánica a Nueva España, autorizada por Carlos III de España (1716-1788). Organizada por el médico y naturalista español <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXVIII/MartinSesse.php">Martín de Sessé y Lacasta </a>(1751-1808) radicado en Nueva España, con apoyo del médico botánico Casimiro Gómez Ortega (1741-1818) director del Real Jardín Botánico de Madrid la expedición se convirtió en una realidad. En la expedición participaron botánicos españoles y yo fui invitado en 1790 por mis conocimientos botánicos. El objetivo de la expedición era recorrer los dominios del imperio, conocer el potencial de sus colonias e ilustrar los documentos de Francisco de Hernández.</p>
<p>La expedición se dividió en tres rutas principales. En la primera, entre 1787 y 1791, recorrimos juntos con Sessé, los pintores Atanasio Echeverria y Vicente de la Cerda, los estados de Michoacán, Nayarit, Jalisco y Sonora.</p>
<p>En la segunda expedición en 1792 zarpamos Atanasio y yo del puerto de San Blas, Nayarit. Navegamos en las goletas “Sutil” y “Mexicana” (de 14 metros de largo), comandadas por Dionisio Alcalá Galiano y Cayetano Valdés y Flores que fueron construidas en tan solo dos meses. También nos acompañó José Cardero, quien se volvió ilustrador y pintó algunos de los biodiversidadajes por los que navegamos. También navegó con nosotros el almirante peruano Juan Francisco de la Bodega y Quadra (1743-1794) en la fragata de Guerra Santa Gertrudis (de 45 metros de largo) para reconocer los límites noroeste de la Nueva España, en lo que hoy es el centro occidente de la Isla de Vancouver en Canadá. Ahí nos encontramos con el Capitán inglés George Vancouver (1757-1798) y sus grandes embarcaciones la HMS (Buque de su majestad) Discovery (de 51 metros de largo) y el HMS Chatham (de 24 metros de largo). En la isla de Nutka conocí a los indígenas Nuu-chah-nulth y a su gran líder Macuina (Maquinna), de los cuales escribí sobre su cultura y tradiciones en “Noticias de Nutka” y en el “Diccionario de la Lengua de los Nutkenses”.</p>
<p>En la tercera expedición nos dividimos la tarea. Entre 1775 a 1778, Sessé y Echeverría salieron a recorrer Cuba y Puerto Rico, mientras yo y Cerda nos fuimos con rumbo a Veracruz, Oaxaca, Chiapas, Guatemala y Nicaragua. Sobre esta expedición escribí: “Flora de Guatemala”, y el “Tratado del xiquilite y del añil de Guatemala”.</p>
<p>La Real Expedición Botánica fue de las más importantes en su tiempo debido a su duración, los territorios que recorrió y la calidad y cantidad del material que se colectó. De ella se originaron varias colecciones botánicas. Nos acompañaron los jóvenes pintores Atanasio Echeverría y Godoy y Juan de Dios Vicente de la Cerda, exalumnos de la Real Academia de Artes de San Carlos quienes produjeron aproximadamente 2,000 ilustraciones y acuarelas, de los cuales 1,800 corresponden a plantas y el resto a varios grupos de peces, anfibios, reptiles, aves, mamíferos, insectos y algunos otros vertebrados.</p>
<p>Junto con mi amigo el naturalista español Martín de Sessé escribi: “Plantae Novae Hispaniae” y “Flora Mexicana”, manuscritos que dieron pie a la clasificación de más de tres mil plantas, muchas de las cuales se identificaron más tarde como nuevas especies. En 1803 Viajé a España junto con Martín de Sessé con los resultados de años de expediciones. Ahí me convertí en presidente de la Real Academia Médica Matritense. Mi gran colega Sessé murió en 1808. En España uno de mis alumnos destacados fue el el naturalista veracruzano <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXVIII/PabloLlave.php">Pablo de la Llave </a>(1773-1833), quien me hizo el honor de darle mi nombre al hermoso quetzal <i>(Pharomachrus moccino).</i></p>
<p>Después de la invasión francesa encabezada por Napoleón I Bonaparte (1769-1821), fui encarcelado por simpatizar con los franceses y finalmente fui desterrado. En Montpellier, Francia, conocí al naturalista suizo Augustin Pyramide de Candolle (1778-1841) quien al saber que regresaría a España me pidió le permitiera calcar las ilustraciones. De Candolle me ayudó a conseguir trabajo en la Universidad de Ginebra, en Suiza. En esos tiempos se desataba la guerra de independencia de México, del otro lado del Atlántico.</p>
<p>En 1818 regresé a España, y fallecí en Barcelona dos años más tarde (12 de junio de 1820). Ha sido una larga historia…. El naturalista y médico español Francisco Hernández de Toledo llevó a cabo la exploración de la Nueva España de 1574 a 1577. Su trabajo se perdió por muchos años y reapareció en 1780. La Real Expedición de la que fui parte (1787 a 1803) tuvo como objetivo explícito “formar los dibujos, recoger las producciones naturales e ilustrar y completar los escritos del doctor don Francisco Hernández”. Mi trabajo con todo y alrededor de 2,000 ilustraciones también se perdió en España y fue encontrado en 1981, 160 años después de mi muerte. Finalmente en 2010, después de 180 años de mi expedición, se publicaron 12 tomos de “La Real Expedición Botánica a Nueva España” con mi labor científica y la de los famosos ilustradores Echeverría y De la Cerda gracias a la UNAM y a Siglo XXI editores.</p>
