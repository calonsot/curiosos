												<h2>José Mariano Mociño</h2>
												<h3>1757 - 1820</h3>
											</header>
<p>José Mariano Mociño fue un ilustre naturalista que nació en 1757 en Real de Minas de Temascaltepec, hoy estado de México, y murió en España en 1820. Sus primeros estudios los realizó en teología y posteriormente en medicina en la Real y Pontificia Universidad de México. Apenas inaugurado el Real Jardín Botánico de la Nueva España en 1788, tomó un curso de botánica y destacó tanto en esta ciencia que en 1789 fue invitado a participar como miembro de la Real Expedición Botánica a Nueva España, organizada por la corona española. El objetivo de la expedición era recorrer los dominios del imperio y conocer el potencial de sus colonias. </p>
                                          <p>La expedición duró de 1787 a 1803 y cubrió desde Nicaragua hasta el Golfo de California. Mociño también se incorporó a dos expediciones complementarias, una en la costa occidental de Canadá y otra en Guatemala de las cuales escribió: &ldquo;Noticias de Nutka&rdquo; y &ldquo;Flora de Guatemala&rdquo;, respectivamente. La Real Expedición Botánica fue de las más importantes en su tiempo debido a su duración, los territorios que recorrió y la calidad y cantidad del material que se colectó. De ella se originaron varias colecciones botánicas y se produjeron aproximadamente 2,000 ilustraciones y acuarelas, de los cuales 1,800 corresponden a plantas y el resto a varios grupos de peces, anfibios, reptiles, aves, mamíferos, insectos y algunos otros vertebrados.</p>
                                          <p>Por su parte, Mociño en colaboración con el naturalista español Martín de Sessé, escribió: &ldquo;Plantae Novae Hispaniae&rdquo; y &ldquo;Flora Mexicana&rdquo;, manuscritos que dieron pie a la clasificación de más de tres mil plantas, muchas de las cuales se identificaron más tarde como nuevas especies. </p>
