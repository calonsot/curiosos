												<h2>Antonio Pineda y Ramírez</h2>
												<h3>1751 – 1792</h3>
											</header>
<p>Nací en el 17 de enero de 1751 en la ciudad de Guatemala, en la Capitanía General de Guatemala, que en aquel entonces incluía Chiapas, Guatemala, San Salvador, Honduras, Nicaragua y Costa Rica. Mi padre fue José Pineda y Tavares y mi madre María Josefa Ramírez y Maldonado. A los 9 años ingresé, junto con mi hermano Mariano, al Real Seminario de Nobles de Madrid y a los 17 entré a la carrera militar como cadete del cuerpo de Reales Guardias Españolas. En el ejercito obtuve el grado de teniente.</p>
<p>Más tarde me dediqué con profundidad al estudio de la física y de la historia natural. Fui alumno del gran botánico español Casimiro Gómez Ortega (1741-1818). Pasé mucho tiempo leyendo en la biblioteca del Real Gabinete de Historia Natural, actualmente el Museo Nacional de Ciencias Naturales de España. 
    <p>En 1788 fui invitado para formar parte de la expedición de <a href="https://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXVIII/AlejandroMalaspina.php">Alejandro Malaspina</a>. Se me encomendaría la tarea del acopio de objetos para el Real Gabinete de Historia Natural, y de plantas vivas para el Real Jardín Botánico. Para estas tareas recomendé la contratación del botánico franco-español Luis Née (1734-1803) y del pintor español José Guío Sánchez. También nos acompañó por una parte del recorrido el naturalista checo Tadeo Haenke (1761-1817)</p>
    <p>Un año más tarde, el 30 de julio de 1789 inicié la travesía del Atlántico a bordo de la corbeta <i>Descubierta</i> comandada por el mismos Malaspina, rumbo a Montevideo, hoy Uruguay. La expedición continuó hacia el sur rumbo al extemo sur del continente americano. Después nos dirigimos hacia el norte, pasando por lo que hoy es Chile, Perú, Ecuador, Panamá hasta llegar al puerto de Acapulco en la Nueva España el 27 de marzo de 1791. </p>
    <p>El recorrido de Acapulco a México duró casi un mes del 8 de mayo al 4 de junio, y pasamos por Chilpancingo, Iguala, Taxco, Cuernavaca, Tlalpan hasta llegar a la Ciudad de México. En la Nueva España pasé ocho meses, donde nos recibió y brindó su apoyo Juan Vicente de Güemes Pacheco de Padilla y Horcasitas (1738-1799), conde de Revillagigedo. Ahí conocí al <a href="https://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXVIII/JoseAlzate.php"> sabio José Antonio de Alzate, </a>con quien hice varios recorridos de campo alrededor de la Ciudad de México entre junio y agosto. Visitamos Tacubaya, Santa Fé, el Cerro de Guadalupe, la Sierra Nevada, la hacienda de San Isidro y Texcoco, el volcán El Fraile, el Desierto de los Leones, Huehuetoca, San Ángel y Xochimilco. </p>
    <p>En la visita a las lagunas de capital, Alzate me mostró a los mexclapiques, peces vivíparos, desconocidos por mi hasta entonces. También me llamó mucho la atención el ajolote <a href="http://enciclovida.mx/especies/35127-ambystoma-mexicanum"><i>(Ambystoma mexicanum)</i> </a> del mismo lago, al cual nombramos "especie de pez cuadrúpedo". Conocí al farmacéutico español Vicente Cervantes Mendo (1755-1829) y al médico y botánico español <a href="https://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXVIII/MartinSesse.php">Martín Sesse y Lacasta de la Real Expedición Botánica a Nueva España. En los recorrios hacia Guanajuato y Querétaro se incoporaron los pintores José Gutiérrez (1770-1835) y Francisco Lindo, e la Academia de San Carlos.</a> Regresé al puerto de Acapulco a finales de noviembre.</p>	
    <p>A pesar de no ser el mejor ilustrador, a menudo dibujé mis observaciones en los diarios de campo, donde se pueden encontrar paisajes, chozas, plantas, animales, máquinas y diversos objetos. Hice descripciones sobre una gran variedad de temas que me parecían sumamente interesantes como la historia del virreinato, cartografía, geología, metalurgia, botánica, zoología, antropología, economía, química y por supuesto, física.</p>
     
    <p>A pesar de mi prematura muerte, el 6 de julio de 1792 en Badoc, en la costa noroeste de las Filipinas a los 41 años, se me ha considerado la figura central de la Expedición Malaspina.</p>
