												<h2>Alexander Von Humboldt</h2>
												<h3>1769 - 1859</h3>
											</header>
<p>Nací en Berlín el 14 de septiembre de 1769. En ese entonces, Alemania era parte del Reino de Prusia. Mi nombre completo fue Federico Guillermo Enrique Alejandro de Humboldt. Mi padre fue Alexander Georg von Humboldt, oficial del ejército de Federico II el Grande. Mi madre Marie Elizabeth Colomb, era viuda de su primer matrimonio con el Barón Hollwede con quien tuvo un hijo. Tuve una hermana que murió muy joven y mi hermano mayor Wilhelm (1767-1835). Mi padre falleció cuando yo tenía 10 años. </p>

<p>Afortunadamente, con la herencia de su primer marido, mi madre pudo contratarnos excelentes tutores que conocían mucho de ciencia. De niño me apodaban "el pequeño boticario" por mi afición a colectar plantas, hermosas conchas e distinguidos insectos.
Estudié en la prestigiosa Universidad de Göttingen en el centro de lo que hoy es Alemania. Ahí tuve la fortuna de conocer a Johann Georg Adam Forster (1754-1794), un naturalista al que le encantaba viajar y que había acompañado al famoso capitán inglés James Cook (1728-1779) en un viaje al océano Pacífico. Pude viajar con él en Europa e Inglaterra, donde conocí a Sir Joseph Banks, presidente de la Sociedad Real, con quien hice una gran amistad. Me preparé para ser un científico explorador estudiando comercio, idiomas, geología, anatomía astronomía y el uso de instrumentos científicos. Trabajé varios años en el Departamento de Minas del Gobierno Prusiano y durante este tiempo conocí al gran escritor y científico Johann Wolfgang von Goethe (1749-1832) con quien compartí muchas inquietudes. </p>

<p>En 1799 estuve a punto de cumplir el anhelo de mi vida e irme en una expedición alrededor del mundo. Sin embargo, la expedición de pospuso debido a las guerras europeas. Así que desilusionado me fui con <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXVIII/AimeBonpland.php">Aimé Bonpland, </a>botánico y médico francés, con rumbo a unirnos a la expedición de Napoleón Bonaparte a Egipto. Los franceses no nos permitieron viajar por los problemas que tenían con África, así que nos fuimos a España. Ahí visitamos el Museo de Historia Natural en donde se encontraban los resultados de la expedición de <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXVIII/MartinSesse.php">Martín de Sessé y Lacasta</a> y <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXVIII/JoseMMucino.php"> José Mariano Mociño.</a> Finalmente el 5 de junio de 1799 zarpamos en el navío Pizarro del puerto de A Coruña en España con rumbo al nuevo continente. </p>


<p>El viaje nos llevaría por una aventura de 5 años recorriendo Venezuela, Cuba, Colombia, Ecuador, Perú, México (Nueva España) y Estados Unidos. El 22 de marzo de 1803, llegamos mi fiel compañero <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXVIII/AimeBonpland.php">Aimé Bonpland</a>, y el científico ecuatoriano Carlos Montúfar y Larrea al famoso puerto de Acapulco en la fragata "Orúe" habiendo zarpado de Guayaquil, Ecuador. En una recua de mulas viajamos por Guerrero hasta llegar la Ciudad de México, la más grande en el continente. Desde la Ciudad de México visité las minas de Guanajuato. Me impresioné con las culturas prehispánicas y escribí un documento sobre la Piedra del Sol, descubierta tan solo 13 años antes de mi llegada. De México, viaje a Veracruz y de ahí a Cuba y a Washington, para encontrarme con el presidente Thomas Jefferson, quien estaba interesado en las ciencias naturales. Regresé a Bourdeaux, Francia, el 3 de agosto de 1804. </p>

<p>Aunque a muchos les pareció antipatriótico, durante la guerra entre Prusia y Francia decidí irme a vivir a París. Ahí me quedé alrededor de veinte años y conocí al gran venezolano Simón Bolívar con quien mantuve contacto hasta su inesperada muerte en 1830. Entre 1816 y 1831 publiqué la obra <strong>"Viaje a las Regiones Equinocciales del Nuevo Continente" </strong>integrada por 13 volúmenes, donde describo el gran viaje por Sudamérica, el Caribe y Norteamérica. En 1827 decidí sentar cabeza y establecerme en Berlín donde me volví consejero del Rey Federico Guillermo III de Prusia y me dediqué a hacer investigaciones y dar charlas públicas. Ese año, el presidente de México, Guadalupe Victoria me dio la ciudadanía mexicana. </p>

<p>Cuando cumplí 60 años en 1829, pude hacer una gran expedición de 8 meses en Rusia invitado por el Zar Nicolás I. Recorrimos desde el río Nevá en San Petersburgo hasta el río Yeniséi en Siberia, una distancia de 15,472 kilómetros. A los sesenta y cinco años empecé a escribir <strong>"Cosmos, o ensayo de una descripción física del mundo".</strong> Ahí quería incluir todo el conocimiento de cielo y tierra. Integré esta obra en 5 volúmenes de los cuales el último se publicó después de mi muerte. </p>

<p>En 1842 durante un viaje apresurado que hice a Londres tuve un encuentro con el joven <a href="http://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXIX/DarwinWallace.php">Charles Darwin. </a>Él tenía 32 años y yo 72. Conocía algunos de sus escritos. Mi amigo el geólogo Roderick Murchinson organizó la reunión en su casa, cerca del palacio de Buckingham. Pobre Darwin, durante tres horas tuvo que escucharme. No le di nunca la oportunidad de hablar. </p>

<p>Fallecí en Berlín el 6 de mayo de 1859 a la edad de 90 años. Ese año, el 29 de junio, el presidente de México, Benito Juárez me nombró "Benemérito de la Patria".</p>


