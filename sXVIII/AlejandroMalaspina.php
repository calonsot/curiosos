												<h2>Alessandro Malaspina</h2>
												<h3>1754 – 1809</h3>
											</header>
<p>Nací en Mulazzo, en la región Toscana en el noroeste de Italia, el 5 de noviembre de 1754. Mi padre fue Carlo Morelo y mi madre Caterina Melilupi, princesa de la casa Soragna. A los 8 años nos mudamos a Palermo, cerca de mi tío el virrey de Sicilia Giovanni Fogliani de Aragón. Más tarde me mude a Roma a estudiar. Me incié en la navegación a los 19 años con la Orden de Malta, una orden religiosa católica.</p>
    <p>A los 20 años ingresé a la Escuela de Guardias Marinas de Cadiz e inicié mi carrera en la Real Armada Española, una de las fuerza navales más antiguas del mundo. Muy pronto me encontraba viajando por el Océano Índico y el Mar de China e hice un viaje de circunnavegación. </p>
    <p>En 1788, a los 34 años, le propuse al gobierno de España realizar una expedición alrededor del mundo con objetivos políticos y científicos y un año más tarde zarpamos de Cadiz en las corbetas "Descubierta" y "Atrevida" construidas específicamente para la expedición. Las nombré en honor a los barcos del explorador británico James Cook (1728-1779). Yo comandé la primera y el marino español José Bustamante y Guerra (1759-1825) iba al frente de la segunda. Nuestras naves eran de última generación, además del armamento de 16 cañones, llevabamos pararrayos, biblioteca y laboratorio con instrumental para llevar a cabo mediciones náuticas, físicas, químicas, biológicas, meteorológicas y astronómicas. Fueron construidas para poder navegar con todo este equipo científico.</p>
    <p>Partimos en la mañana del 30 de julio de 1789 del puerto de Cádiz y nos llevamos un mes y medio para arribar a Montevideo en Sudamérica. En la tripulación había toda clase de profesiones: naturalistas, astrónomos, hidrógrafos, cartógrafos, carpinteros, herreros, pilotos, pintores, artilleros y hasta un capellán.</p>
    <p>Nos llevó varios meses recorrer el Atlántico y el Pacífico sudamericano obteniendo información de distintas disciplinas. El 2 de febrero de 1790 llegó la <i>Atrevida </i>a la Bahía de Acapulco. Casi un año más tarde llegó la <i>Descubierta</i>, el 27 de marzo 1791. </p>
    <p>De Acapulco visité la capital de la Nueva España para entrevistarme con el Virrey Juan Vicente de Güemes Pacheco y Padilla, segundo conde de Revillagigedo. Allí pude conocer al círculo de ilustrados como <a href="https://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXVIII/JoseAlzate.php">José Antonio Alzate y Ramírez,</a> Antonio León y Gama (1735-1802), Miguel Constanzo (1741-1814), entre otros. Apenas unos meses antes se había desenterrado la impresionante Piedra del Sol o Calendario Azteca, al realizar trabajos para mejorar el drenaje y las banquetas de la ciudad. Alzate nos recomendó "Historia Antiua de México", el libro de su maestro <a href="https://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXVIII/FcoClavijero.php">Francisco Xavier Clavijero,</a> jesuita exilado en Europa. En México tuve instrucciones de dirigir la expedición hacia el norte para buscar el paso hacia el Atlántico. También decidí dejar en Nueva España a una comisión científica dividida en dos grupos: el de historia natural, integrada por <a href="https://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXVIII/AntonioPineda.php">Antonio Pineda, </a> Luis Neé, José Guio y Julián del Villar, y  la de Geografía y Astronomía, al mando de Dionisio Alcalá Galiano, incluía a Arcadio Pineda, Martín de Olavide y Manuel Novales. Por su parte siguieron hacia el Pacífico norte, el botánico Tadeo Haenke y los pintores Tomás de Suria y José Cardero.</p>
    
    <p>Si hay algo que intenté fue dejar plasmado en ilustraciones toda la expedición. Me esforzé en tener siempre un buen número de pintores de gran experiencia. Entre ellos me acompañaron Tomás de Suria (Academia de San Carlos), Juan Ravenet, Fernando Brambila, José Guío, José Cardero, Felipe Bauzá, <a href="https://www.biodiversidad.gob.mx/biodiversidad/curiosos/sXVIII/AntonioPineda.php"> Antonio Pineda, </a>José Gutiérrez, Francisco Lindo y Julián del Villar y Pardo. </p>

    <p>Durante la expedición acumulamos una gran cantidad de materiales, cartas naúticas, observaciones astronómicas, objetos de historia natural. Cuando regresamos a España presenté el informe "Viaje político-científico alrededor del mundo" con mi punto de vista sobre las colonias de España. Ahí sugería que las colonias tendrían que ser más independientes. Debido a esto fui acusado por el primer ministro del rey Carlos IV, Manuel Godoy y Álvarez de Faria (1767-1851) condenado a diez años de prisión en el castillo de San Antón de la Coruña, en el noroeste de España.</p>   

        <p>Durante siete años que pasé preso en el castillo escribí ensayos sobre estética, economía y literatura y mantuve correspondencia con varios amigos. Gracias a la presión que puso Napoleón Bonaparte, recuperé mi libertad en 1802 y fui deportado a Italia. </p>
        <p>En 1809 fallecí en la comuna de Pontremoli, cerca de donde nací a causa de un infarto a los 55 años.</p>
